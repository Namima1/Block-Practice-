2.upto(10) { |current| puts "The loop is  now on: #{current}" }
#upto method lets us go up to a certain number 
#the 2 indicates where to begin
#the 10 indicated which number to stop at
#curly braces is the block

5.downto(1) { |number| puts "Countdown: #{number}" }
#5 is the integer it starts
#1 is the integer it gets to 